/**
 * Get all areas as array
 */
export function getAllAreas() {
  return Object.values(areas);
}

/**
 * Get single area by slug
 */
export function getAreaBySlug(slug) {
  return areas[slug.toLowerCase()] || null;
}

/**
 * Check if area exists
 */
export function areaExists(slug) {
  return !!areas[slug.toLowerCase()];
}

/**
 * Get areas by rating (5 = highest)
 */
export function getAreasByNomadRating(minRating = 1) {
  return Object.values(areas).filter(area => area.nomadRating >= minRating);
}

/**
 * Get areas by budget range
 */
export function getAreasByBudget(maxRent) {
  return Object.values(areas).filter(area => area.averageRent <= maxRent);
}
